    private void jButtonAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAddMouseClicked
        String producto = jTextFieldProducto.getText();
        
        modeloListaCompra.addElement(producto);
    }//GEN-LAST:event_jButtonAddMouseClicked

    private void jButtonDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDeleteMouseClicked
        int pos;
        if (!jListProductos.isSelectionEmpty()) {
            pos=jListProductos.getSelectedIndex();
            modeloListaCompra.remove(pos);
        }else
            JOptionPane.showMessageDialog(rootPane, "No hay ningun producto seleccionado para borrar", "Error", 2);
    }//GEN-LAST:event_jButtonDeleteMouseClicked

    private void jTextFieldBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldBuscarKeyReleased
        
        if (!jTextFieldBuscar.getText().equals("")) {
            String texto=jTextFieldBuscar.getText();
        
        for (int i = 0; i < modeloListaCompra.getSize(); i++) {
            if (modeloListaCompra.get(i).startsWith(texto)) {
                modeloBusqueda.addElement(modeloListaCompra.get(i));
            }
        }
        jListProductos.setModel(modeloBusqueda);
        }else
            jListProductos.setModel(modeloListaCompra);
        
        
    }//GEN-LAST:event_jTextFieldBuscarKeyReleased

